package com.example.ordersystem.pojo;

import java.util.List;

public class BlanketOrder {
    private int o_id, u_id;
    private double o_totalPrice;
    private String message, time, u_name;
    private List<OrderItem> orderItems;

    public int getO_id() {
        return o_id;
    }

    public void setO_id(int o_id) {
        this.o_id = o_id;
    }

    public int getU_id() {
        return u_id;
    }

    public void setU_id(int u_id) {
        this.u_id = u_id;
    }

    public double getO_totalPrice() {
        return o_totalPrice;
    }

    public void setO_totalPrice(double o_totalPrice) {
        this.o_totalPrice = o_totalPrice;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }

    public List<OrderItem> getOrderItems() {
        return orderItems;
    }

    public void setOrderItems(List<OrderItem> orderItems) {
        this.orderItems = orderItems;
    }
}
